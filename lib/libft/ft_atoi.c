#include "libft.h"

int	ft_atoi(const char *nptr)
{
	int	i;
	int	s;
	int	j;

	i = 0;
	s = 1;
	j = 0;
	while ((nptr[i] >= '\t' && nptr[i] <= '\r') || nptr[i] == ' ')
		i++;
	if (nptr[i] == '+' || nptr[i] == '-')
	{
		if (nptr[i] == '-')
			s *= -1;
		i++;
	}
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		j = (nptr[i] - '0') + (j * 10);
		i++;
	}
	return (j * s);
}
